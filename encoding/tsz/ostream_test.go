package tsz

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestWriteBits(t *testing.T) {
	inputs := []struct {
		value         uint64
		numBits       int
		expectedBytes []byte
		expectedPos   int
	}{
		{0x1, 1, []byte{0x1}, 1},
		{0x4, 3, []byte{0x9}, 4},
		{0xa, 4, []byte{0xa9}, 8},
		{0xfe, 8, []byte{0xa9, 0xfe}, 8},
		{0xaafe, 7, []byte{0xa9, 0xfe, 0x7e}, 7},
		{0x3, 2, []byte{0xa9, 0xfe, 0xfe, 0x1}, 1},
		{0x1234567890abcdef, 64, []byte{0xa9, 0xfe, 0xfe, 0xdf, 0x9b, 0x57, 0x21, 0xf1, 0xac, 0x68, 0x24, 0x0}, 1},
		{0x1, 0, []byte{0xa9, 0xfe, 0xfe, 0xdf, 0x9b, 0x57, 0x21, 0xf1, 0xac, 0x68, 0x24, 0x0}, 1},
		{0x1, 65, []byte{0xa9, 0xfe, 0xfe, 0xdf, 0x9b, 0x57, 0x21, 0xf1, 0xac, 0x68, 0x24, 0x2, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0}, 1},
	}

	os := newOStream()
	require.True(t, os.empty())
	for _, input := range inputs {
		os.writeBits(input.value, input.numBits)
		require.Equal(t, input.expectedBytes, os.rawBuffer)
		require.Equal(t, input.expectedPos, os.pos)
	}
	require.False(t, os.empty())
}

func TestResetOStream(t *testing.T) {
	os := newOStream()
	os.writeByte(0xfe)
	os.reset()
	require.True(t, os.empty())
	require.Equal(t, 0, os.len())
	require.Equal(t, 0, os.pos)
}
